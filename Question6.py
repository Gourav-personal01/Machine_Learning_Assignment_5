# Q6. You are working with a dataset containing information about different types of animals, including their
# species, habitat, and diet. Which encoding technique would you use to transform the categorical data into
# a format suitable for machine learning algorithms? Justify your answer.

# Aanswer - 

# For categorical data about animal species, habitat, and diet, I would use one-hot encoding. This technique creates binary columns for each unique category,
# ensuring that the machine learning algorithms can handle categorical
# data without assuming any ordinal relationships between categories. 
# It's a suitable choice for preserving the distinct characteristics of each category.