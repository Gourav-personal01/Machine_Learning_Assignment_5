# Q5. In a machine learning project, you have a dataset with 1000 rows and 5 columns. Two of the columns
# are categorical, and the remaining three columns are numerical. If you were to use nominal encoding to
# transform the categorical data, how many new columns would be created? Show your calculations.

# Answer - 

# When using nominal encoding (also known as label encoding) to transform categorical data, you replace each unique category with a corresponding integer label. The number of new columns created is equal to the number of categorical columns in the original dataset.

# In your case, you mentioned that there are two categorical columns. Therefore, when applying nominal encoding, you would create two new columns, each containing integer labels representing the categories in the respective categorical columns.

# No additional columns are created beyond these two when using nominal encoding.